<div class="container">
	<h1>Interested in reading more?</h1>
	<p>
		For summaries about <a
			href="https://en.wikipedia.org/wiki/Aristoxenus"
			target="_blank">Aristoxenus</a>
		and
		<a href="https://en.wikipedia.org/wiki/Genus_(music)" target="_blank"
			>Greek music theory</a> music theory in general, Wikipedia is a great
		place to start. However, if you want to do some deeper diving, here are a
		few articles and books that we're particularly inspiring to me and my project.
	</p>
	<div>
		<h5>
			Macran, Henry S. <a
				class="fst-italic"
				href="https://archive.org/details/harmonicsaristo00arisgoog/page/n6/mode/2up"
				target="_blank">The Harmonics of Aristoxenus</a
			>. Oxford: Clarendon Press, 1902.
		</h5>
		<p>
			The translation and commentary that drove the majority of my
			project. Without this particular text, I wouldn’t have been able to
			engage with the source text as closely as I did.
		</p>
	</div>
	<div>
		<h5>
			Chalmers, John H. (1993). Polansky, Larry; Scholz, Carter (eds.). <a
				class="fst-italic"
				href="https://eamusic.dartmouth.edu/~larry/published_articles/divisions_of_the_tetrachord/index.html"
				target="_blank"
				>Divisions of the Tetrachord: A prolegomenon to the construction
				of musical scales</a>
			. Harrison, Lou (foreword). Hanover, NH: Frog Peak Music. ISBN 0-945996-04-7.
		</h5>
		<p>
			This website is an interactive chapter-by-chapter breakdown of the
			book in the citation. I drew primarily from the 3rd chapter on
			Aristoxenus and what they referred to as the “geometrization of
			musical space,” which helped me visualize the divisions of the
			tetrachord, since Aristoxenus’ words were vague in many cases. It
			also introduced me to another system that involved dividing the
			Fourth into thirty parts by Cleonides, as well as fraction divisions
			of the tone and cents.
		</p>
	</div>
	<div>
		<h5>
			Levin, Flora (1972). <a
				class="fst-italic"
				href="https://www.jstor.org/stable/2935976?seq=3"
				target="_blank">Synesis in Aristoxenian Theory</a
			>. Transactions and Proceedings of the American Philological
			Association. 103: 211–234.
		</h5>
		<p>
			This was a handy introduction to <span class="fst-italic"
				>synesis</span> which is related to sensory perception, but focused
			primarily on the intuition that we have about music that stems from it.
		</p>
	</div>
	<div>
		<h5>
			Barker, Andrew (1978). <a
				class="fst-italic"
				href="https://www.jstor.org/stable/630189"
				target="_blank">Music and Perception: A Study in Aristoxenus</a
			>. Journal of Hellenic Studies. 98: 9–16.
		</h5>
		<p>
			I found that one of the best qualities of this article was
			introducing the concept of theory being driven by sensory perception
			and how it contrasted with the math dominated theory that preceded
			Aristoxenus.
		</p>
	</div>
</div>
