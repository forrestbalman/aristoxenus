			differences of the genera
		</a>
		in the <span class="fst-italic">Elementa Harmonica</span>, Artistoxenus
		summarizes the tetrachords (four note pitch collections that span a
		perfect Fourth) based on their intervals, genus, and
		<span class="fst-italic">shade</span>.
	</p>
</div>
<div>
	<h5>The degrees of the tetrachord from lowest to highest are:</h5>
	<ol>
		<li>Hypate</li>
		<li>Parhypate</li>
		<li>Lichanus</li>
		<li>Mese</li>
	</ol>
</div>
<div>
	<h5>Guidelines for tetrachord structure</h5>
	<ol>
		<li>
			The outer interval (Hypate to Mese) is a perfect Fourth (500 cents).
		</li>
		<li>
			The interval from Hypate to Parhypate will always be between 1/4 (50
			cents) and 1/2 of a tone (100 cents). It will also be the smallest
			interval in the tetrachord.
		</li>
		<li>
			The interval from Hypate to Lichanus will always be between 1/2 tone
			(50 cents) and 1 and 1/2 tones (300 cents).
		</li>
		<li>
			The interval from Lichanus to Mese will always be the remaining
			interval that completes the perfect Fourth, which is between
			(inclusive) 1 (200 cents) and 2 tones (400 cents).
		</li>
	</ol>
</div>
<div>
	<h5>Tetrachord Genera</h5>
	<p>
		A tetrachord's genus (plural, genera) is defined by the distance between
		Lichanus and Mese. The genus also has a particular <span
			class="fst-italic">shade</span> based on where in the range it falls.
		There are three different genera (or flavors) of tetrachord:
	</p>
	<ul>
		<li>
			Enharmonic - greater than 1 and 5/6 tones (about 367 cents) to equal
			to 2 tones (400 cents)
		</li>
		<li>
			Chromatic - greater than 1 and 1/4 tones (250 cents) to less than or
			equal to 1 and 5/6 tones (about 367 cents)
		</li>
		<li>
			Diatonic - greater than or equal to 1 tone (200 cents) to 1 and 1/4
			tones (50 cents)
		</li>
	</ul>
	<p>
		This means there are, technically, infinite tetrachord possibilities.
		However, Aristoxenus describes 6 noteworthy shades of tetrachord, 1
		Enharmonic, 3 Chromatic, and 2 Diatonic:
	</p>
	<ul>
		<li>Enharmonic - 1/4 + 1/4 + 2 tones (50, 50, and 400 cents)</li>
		<li>
			Soft Chromatic - 1/3 + 1/3 + 1 and 5/6 tones (all approximations 67,
			67, and 367 cents)
		</li>
		<li>
			Hemiolic Chromatic - 3/8 + 3/8 + 1 and 3/4 tones (75, 75, and 350
			cents)
		</li>
		<li>
			Tonic Chromatic - 1/2 + 1/2 + 1 and 1/2 tones (100, 100, and 300
			cents)
		</li>
		<li>
			Soft Diatonic - 1/2 + 3/4 + 1 and 1/4 tones (100, 150, and 250
			cents)
		</li>
		<li>Sharp Diatonic - 1/2 + 1 + 1 tones (100, 200, 200 cents)</li>
	</ul>
</div>
<div>
	<h5>Dieses</h5>
	<p>
		The term <span class="fst-italic">diesis</span> (plural, dieses) refers to
		the smallest interval in the tetrachord when it's less than 1/2 tone (you
		can read more about it in the Book 1 section).
	</p>
	<ul>
		<li>
			An Enharmonic diesis (or the smallest Enharmonic diesis) is 1/4 tone
			(50 cents)
		</li>
		<li>A smallest Chromatic diesis is 1/3 tone (about 67 cents)</li>
		<li>A Hemiolic Chromatic diesis is 3/8 tone (about 75 cents)</li>
	</ul>
	<p>
		Keeping this in mind, we could create a series of ranges that describe
		Enharmonic and Chromatic dieses:
	</p>
	<ul>
		<li>
			Enharmonic - greater than or equal to 1/4 tone and less than 1/3
			tone
		</li>
		<li>
			Chromatic - greater than or equal to 1/3 tone and less than 1/2 tone
		</li>
	</ul>
</div>
